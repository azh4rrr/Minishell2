#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		del((*lst)->key);
		del((*lst)->value);
		free(*lst);
		*lst = temp;
	}
	*lst = NULL;
}
